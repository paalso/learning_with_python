# 10. Write a function find_hypot which, given the length of two sides
##of a right-angled triangle, returns the length of the hypotenuse.
##(Hint: x ** 0.5 will return the square root.)

def find_hypot (a, b):
    return (a * a + b * b) ** 0.5

